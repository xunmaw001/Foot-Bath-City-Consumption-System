			wPicker
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.shangjiazhanghao = this.user.shangjiazhanghao
			this.ro.shangjiazhanghao = true;
			this.ruleForm.shangjiaxingming = this.user.shangjiaxingming
			this.ro.shangjiaxingming = true;


			// 自定义下拉框值
			this.baoxiangmianjiOptions = "大,中,小".split(',')
			// 自定义下拉框值
			this.baoxiangzhuangtaiOptions = "已使用,空闲".split(',')

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`baoxiangxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='baoxianghao'){
					this.ruleForm.baoxianghao = obj[o];
					this.ro.baoxianghao = true;
					continue;
					}
					if(o=='baoxiangmingcheng'){
					this.ruleForm.baoxiangmingcheng = obj[o];
					this.ro.baoxiangmingcheng = true;
					continue;
					}
					if(o=='baoxiangtupian'){
					this.ruleForm.baoxiangtupian = obj[o];
					this.ro.baoxiangtupian = true;
					continue;
					}
					if(o=='baoxiangmianji'){
